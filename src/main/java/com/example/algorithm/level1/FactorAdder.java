package com.example.algorithm.level1;

/**
 * FactorAddor 약수의 합
 * 
 * 정수 n을 입력받아 n의 약수를 모두 더한 값을 리턴하는 함수, solution을 완성
 * 
 * @see https://school.programmers.co.kr/learn/courses/30/lessons/12928
 */
public interface FactorAdder {
    /**
     * 정수를 입력받아 n의 약수를 모두 더한 값을 리턴
     * 
     * @param number 정수, 0 이상 3000이하인 정수
     * @return 약수의 합
     */
    public int solution(int number);
}
